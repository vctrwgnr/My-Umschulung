<h3>Summary of Modules and What I Have Learned</h3>

<p>
  <strong>PC Basics</strong>: This module introduced the fundamentals of personal computers, including key hardware components like processors, 
  memory, and storage devices, as well as basic software operations and the functionality of different operating systems.
</p>

<p>
  <strong>Economics and Social Studies</strong>: In this module, I learned about the 
  BGB (German Civil Code), commercial law, general terms and conditions, product liability, 
  tax law, labor law, social law, and employee rights.
</p>


<p>
  <strong>Network Basics</strong>: This module provided a foundation in networking concepts, including different types 
  of networks (LAN, WAN) and protocols like TCP/IP, DNS, and DHCP. I also gained practical experience in setting up 
  and configuring small networks. Additionally, I learned about virtualization technologies such as Hyper-V, 
  VMware, and Oracle VM, which play a crucial role in network management and infrastructure optimization.
</p>


<p>
  <strong>Operating System Basics</strong>: Here, I explored the installation, configuration, and management of operating systems, 
  with a specific focus on Linux. I learned to work with the command line, manage user permissions, and optimize system performance.
</p>

<p>
  <strong>Programming Basics</strong>: This module covered the essential principles of programming, including logic and syntax. 
  I gained hands-on experience with languages like PHP, JavaScript and Python, and learned about basic programming constructs, control flow, and functions.
</p>

<p>
  <strong>Database Basics</strong>: In this module, I was introduced to databases, particularly relational databases, and SQL queries. 
  I gained experience working with MySQL and MariaDB, including creating databases, performing CRUD (Create, Read, Update, Delete) operations, and managing data efficiently.
</p>

<p>
  <strong>IT System Technology</strong>: This module explored various IT systems used in business environments. 
  I gained an understanding of server and client architectures, virtualization technologies, and cloud computing systems, with practical experience in system administration using Linux.
</p>

<p>
  <strong>Project Management</strong>: I learned the essentials of project management, including planning, execution, and monitoring. 
  Topics covered included task scheduling, risk management, and resource allocation.
</p>

<p>
  <strong>Programming</strong>: Building upon the basics, this module delved deeper into programming concepts like Object-Oriented Programming (OOP), 
  covering classes, inheritance, and polymorphism. I gained experience in building applications with PHP, HTML, CSS, and JavaScript, working on web development projects that included database integration with MySQL and CRUD functionality.
</p>

   